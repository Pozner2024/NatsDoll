    const result = await makeUploadProductImage(upload)({
      bytes: jpegBytes,
      contentType: 'image/jpeg',
    })

    expect(upload).toHaveBeenCalledOnce()
    const [key, body, contentType] = upload.mock.calls[0]!
    expect(key).toMatch(/^items\/new\/[0-9a-f-]+\.webp$/)
    expect(contentType).toBe('image/webp')
    // результат — валидный WebP (магические байты RIFF....WEBP)
