//Вместо того чтобы вручную писать сложный код (который бы проверял,
//  куда вставить запятую, где поставить значок доллара и как округлить копейки), 
// мы используем Intl — это встроенный в каждый современный браузер
//  мощный инструмент для интернационализации. Браузер сам знает все
// s правила форматирования денег в любой стране мира.
const formatter = new Intl.NumberFormat('en-US', {
  style: 'currency',
  currency: 'USD',
  minimumFractionDigits: 2,
  maximumFractionDigits: 2,
})

/**
 * Форматирует числовое значение в строку с денежным форматом ($XX.XX).
 * Использует встроенный в браузер форматтер (Intl.NumberFormat) для корректного отображения
 * валюты в зависимости от региона пользователя.
 * 
 * @param value - Число, которое нужно отформатировать (например, 499.99).
 * @returns Отформатированная строка (например, "$499.99").
 */
export function formatPrice(value: number): string {
  return formatter.format(value)
}
